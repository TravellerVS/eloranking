package com.vedransemenski.eloranking.business;

public class PlayerStats {

    int numberOfWins;
    int getNumberOfLoses;
    int score;

    public int getNumberOfWins() {
        return numberOfWins;
    }

    public void setNumberOfWins(int numberOfWins) {
        this.numberOfWins = numberOfWins;
    }

    public int getGetNumberOfLoses() {
        return getNumberOfLoses;
    }

    public void setGetNumberOfLoses(int getNumberOfLoses) {
        this.getNumberOfLoses = getNumberOfLoses;
    }

    public int getScore() {
        return score;
    }

    public void setScore(int score) {
        this.score = score;
    }
}
